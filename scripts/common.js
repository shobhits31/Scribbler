function showSignUp() {
  var signUpModal = document.getElementById("signup-pop-up-modal");
  signUpModal.style.display = "flex";
}

function hideSignUp() {
  var signUpModal = document.getElementById("signup-pop-up-modal");
  signUpModal.style.display = "none";
}

